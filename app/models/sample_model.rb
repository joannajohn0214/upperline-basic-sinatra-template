def baking_generator(user_value)
  
  if user_value == 6
    return "tenMinAlright"
  elsif user_value == 7
    return "tenMinChallenge"
  elsif user_value == 11
    return "thirtyMinAlright"
  elsif user_value == 12
    return "thirtyMinChallenge"
  elsif user_value == 16
    return "hourAlright"
  elsif user_value == 17
    return "hourChallenge"
  end

end